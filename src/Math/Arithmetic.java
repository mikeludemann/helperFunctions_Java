package Math;

import java.lang.*;
import java.util.*;

public class Arithmetic {

    public static double plus(double firstInput, double secondInput) {
        double result = firstInput + secondInput;
        return result;
    }

    public static double minus(double firstInput, double secondInput) {
        double result = firstInput - secondInput;
        return result;
    }

    public static double multiply(double firstInput, double secondInput) {
        double result = firstInput * secondInput;
        return result;
    }

    public static double division(double firstInput, double secondInput) {
        double result = firstInput / secondInput;
        return result;
    }

    public static double modulo(double firstInput, double secondInput) {
        double result = firstInput % secondInput;
        return result;
    }

}